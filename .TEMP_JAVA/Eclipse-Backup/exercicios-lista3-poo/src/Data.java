
public class Data {
	private int dia, mes, ano;
	
	public void setDia(int dia) {
		this.dia = dia;
	}
	
	public int getDia() {
		return this.dia;
	}

	public int getMes() {
		return mes;
	}

	public void setMes(int mes) {
		this.mes = mes;
	}

	public int getAno() {
		return ano;
	}

	public void setAno(int ano) {
		this.ano = ano;
	}
	
	
}
